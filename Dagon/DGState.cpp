DGState::DGState() {
	_current = DGStateNode;
	_previous = _current;
}

////////////////////////////////////////////////////////////
// Implementation - Destructor
////////////////////////////////////////////////////////////

DGState::~DGState() {
    // Nothing to do here
}

////////////////////////////////////////////////////////////
// Implementation
////////////////////////////////////////////////////////////

int DGState::current() {
    return _current;
}

int DGState::previous() {
    return _previous;
}

void DGState::set(int theState) {
    _previous = _current;
	_current = theState;
}

void DGState::setPrevious() {
    int auxState = _current;
	
	_current = _previous;
	_previous = auxState;
}
